//Trabalhando com vetor
//Vetor é uma lista de dados, ele pode esta dentro de uma variável ou de uma constante
let minhaFamilia = ["Mãe" , "Pai" , "Irmão" , "Irmã" , "Tio" , "Tia"]

//O metodo pop remove o ultimo item da lista(vetor)
minhaFamilia.pop()
console.log(minhaFamilia)

//O metodo shift remove o primeiro item da lista(vetor)
minhaFamilia.shift()
console.log(minhaFamilia)

 